'use client';

import React, { useState, useEffect, useRef } from 'react';

interface SearchSuggestion {
  text: string;
  type: 'history' | 'tag' | 'synonym';
  weight: number;
}

interface SearchInfo {
  original_query: string;
  ai_explanation?: string;
  search_intent?: string;
  query_time?: number;
  expanded_queries: string[];
  suggestions: string[];
}

// ÂÆö‰πâAIÊêúÁ¥¢ÁªìÊûúÁ±ªÂûã
interface AIPoseResult {
  id: number;
  oss_url: string;
  thumbnail_url?: string;
  title: string;
  description: string;
  scene_category: string;
  angle: string;
  ai_tags: string;
  view_count: number;
  created_at: string;
  ai_relevance_explanation?: string;
  shooting_tips?: string;
}


interface Props {
  onSearch: (query: string) => void;
  onAISearchResult?: (poses: AIPoseResult[]) => void;
  onResetSearch?: () => void;
  initialValue?: string;
  showSearchInfo?: boolean;
}

const EnhancedSearchBar: React.FC<Props> = ({ 
  onSearch, 
  onAISearchResult,
  onResetSearch,
  initialValue = '', 
  showSearchInfo = false 
}) => {
  const [query, setQuery] = useState(initialValue);
  const [suggestions, setSuggestions] = useState<SearchSuggestion[]>([]);
  const [showSuggestions, setShowSuggestions] = useState(false);
  const [selectedIndex, setSelectedIndex] = useState(-1);
  const [searchInfo, setSearchInfo] = useState<SearchInfo | null>(null);
  const [isLoading, setIsLoading] = useState(false);
  const [isAiDatabaseLoading, setIsAiDatabaseLoading] = useState(false);
  const [isVectorLoading, setIsVectorLoading] = useState(false);
  const [useVectorSearch, setUseVectorSearch] = useState(false);
  
  const inputRef = useRef<HTMLInputElement>(null);
  const suggestionsRef = useRef<HTMLDivElement>(null);

  // ÁÆÄÂåñÂª∫ËÆÆÂäüËÉΩ - ‰ΩøÁî®Êú¨Âú∞Âª∫ËÆÆËÄå‰∏çÊòØAPIË∞ÉÁî®
  const getLocalSuggestions = (prefix: string): SearchSuggestion[] => {
    const commonSuggestions = [
      { text: '‰øèÁöÆÂèØÁà±', type: 'tag' as const, weight: 0.9 },
      { text: 'ÂíñÂï°ÂéÖÊãçÁÖß', type: 'tag' as const, weight: 0.8 },
      { text: 'ÂùêÂßøÂÜôÁúü', type: 'tag' as const, weight: 0.7 },
      { text: 'Êà∑Â§ñ‰∫∫ÂÉè', type: 'tag' as const, weight: 0.8 },
      { text: 'ÊÉÖ‰æ£ÊãçÁÖß', type: 'tag' as const, weight: 0.9 },
      { text: 'Ë°óÂ§¥ÊëÑÂΩ±', type: 'tag' as const, weight: 0.7 },
      { text: 'ÂÆ§ÂÜÖÂÜôÁúü', type: 'tag' as const, weight: 0.8 },
      { text: 'ÂïÜÂä°ÂΩ¢Ë±°', type: 'tag' as const, weight: 0.6 },
    ];

    return commonSuggestions
      .filter(s => s.text.includes(prefix))
      .slice(0, 5);
  };

  // ÂΩìËæìÂÖ•ÂèòÂåñÊó∂ÊòæÁ§∫Âª∫ËÆÆ
  useEffect(() => {
    if (query && query.length > 0) {
      const localSuggestions = getLocalSuggestions(query);
      setSuggestions(localSuggestions);
    } else {
      setSuggestions([]);
    }
  }, [query]);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setShowSuggestions(false);
    setSearchInfo(null);
    
    if (!query.trim()) {
      if (onResetSearch) {
        onResetSearch();
      }
      return;
    }

    setIsLoading(true);
    
    try {
      onSearch(query);
    } catch (error) {
      console.error('ÊêúÁ¥¢Â§±Ë¥•:', error);
      onSearch(query);
    } finally {
      setIsLoading(false);
    }
  };

  // AIÊï∞ÊçÆÂ∫ìÊêúÁ¥¢ÂäüËÉΩ
  const handleAiDatabaseSearch = async () => {
    if (!query.trim()) {
      if (onResetSearch) {
        onResetSearch();
      }
      return;
    }

    setIsAiDatabaseLoading(true);
    setShowSuggestions(false);
    
    try {
      console.log('ÂºÄÂßãAIÊï∞ÊçÆÂ∫ìÊêúÁ¥¢:', query);
      
      const response = await fetch('/api/search/ai-database', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ 
          query: query.trim(),
          max_results: 20 
        })
      });
        
      if (response.ok) {
        const data = await response.json();
        console.log('AIÊï∞ÊçÆÂ∫ìÊêúÁ¥¢ÂìçÂ∫î:', data);
        
        setSearchInfo({
          original_query: query,
          ai_explanation: data.ai_explanation || '‰ΩøÁî®AIÂàÜÊûêÊâæÂà∞ÊúÄÁõ∏ÂÖ≥ÁöÑÊãçÁÖßÂßøÂäø',
          search_intent: data.search_intent?.intent_type || 'Êô∫ËÉΩÂåπÈÖç',
          query_time: data.query_time_ms,
          expanded_queries: [],
          suggestions: []
        });
          
        if (data.poses && Array.isArray(data.poses) && data.poses.length > 0) {
          console.log('Ë∞ÉÁî®AIÊêúÁ¥¢ÁªìÊûúÂõûË∞ÉÔºåÂßøÂäøÊï∞Èáè:', data.poses.length);
          if (onAISearchResult) {
            onAISearchResult(data.poses);
          } else {
            console.warn('onAISearchResultÂõûË∞ÉÊú™ÂÆö‰πâÔºåÊâßË°åÊôÆÈÄöÊêúÁ¥¢');
            onSearch(query);
          }
        } else {
          console.warn('AIÊêúÁ¥¢ÁªìÊûú‰∏∫Á©∫ÔºåÊâßË°åÊôÆÈÄöÊêúÁ¥¢');
          const fallbackQuery = data.suggested_query || data.corrected_query || query;
          setQuery(fallbackQuery);
          onSearch(fallbackQuery);
        }
      } else {
        const errorText = await response.text();
        console.error('AIÊï∞ÊçÆÂ∫ìÊêúÁ¥¢APIÂìçÂ∫îÈîôËØØ:', response.status, errorText);
        onSearch(query);
      }
    } catch (error) {
      console.error('AIÊï∞ÊçÆÂ∫ìÊêúÁ¥¢Â§±Ë¥•:', error);
      onSearch(query);
    } finally {
      setIsAiDatabaseLoading(false);
    }
  };

  const handleVectorSearch = async () => {
    if (!query.trim()) {
      if (onResetSearch) {
        onResetSearch();
      }
      return;
    }

    setIsVectorLoading(true);
    setShowSuggestions(false);

    try {
      console.log('ÂºÄÂßãÂêëÈáèÊêúÁ¥¢:', query);
      
      // È¶ñÂÖàÊ£ÄÊü•ÂêëÈáèÊêúÁ¥¢ÊúçÂä°Áä∂ÊÄÅ
      const statusResponse = await fetch('/api/search/vector/status');
      let serviceAvailable = true;
      
      if (statusResponse.ok) {
        const statusData = await statusResponse.json();
        serviceAvailable = statusData.available;
        console.log('ÂêëÈáèÊêúÁ¥¢ÊúçÂä°Áä∂ÊÄÅ:', statusData);
      }
      
      if (!serviceAvailable) {
        console.warn('ÂêëÈáèÊêúÁ¥¢ÊúçÂä°‰∏çÂèØÁî®ÔºåÂõûÈÄÄÂà∞ÊôÆÈÄöÊêúÁ¥¢');
        setSearchInfo({
          original_query: query,
          ai_explanation: 'ÂêëÈáèÊêúÁ¥¢ÊúçÂä°ÊöÇÊó∂‰∏çÂèØÁî®ÔºåÂ∑≤‰∏∫ÊÇ®ÊâßË°åÊôÆÈÄöÊêúÁ¥¢',
          search_intent: 'ÊúçÂä°ÈôçÁ∫ß',
          query_time: 0,
          expanded_queries: [],
          suggestions: [],
        });
        onSearch(query);
        return;
      }
      
      const response = await fetch('/api/search/vector', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          query: query.trim(),
          top_k: 20,
          use_adaptive: true,  // ‰ΩøÁî®Ëá™ÈÄÇÂ∫îÊêúÁ¥¢
        }),
      });

      if (response.ok) {
        const data = await response.json();
        console.log('ÂêëÈáèÊêúÁ¥¢ÂìçÂ∫î:', data);

        // ÊûÑÂª∫ÊêúÁ¥¢‰ø°ÊÅØÔºåÂåÖÂê´Ë¥®ÈáèÊåáÊ†á
        let explanation = '‰ΩøÁî®ÂêëÈáèÁõ∏‰ººÂ∫¶ÂåπÈÖçÊâæÂà∞ÊúÄÁõ∏ÂÖ≥ÁöÑÂßøÂäø';
        
        if (data.search_info) {
          const info = data.search_info;
          explanation += `\nÊâæÂà∞ ${info.found_results} ‰∏™ÁªìÊûú`;
          
          if (info.avg_similarity) {
            explanation += `ÔºåÂπ≥ÂùáÁõ∏‰ººÂ∫¶: ${info.avg_similarity}`;
          }
          
          if (info.quality_warning) {
            explanation += `\n‚ö†Ô∏è ${info.quality_warning}`;
          }
          
          if (info.similarity_range) {
            explanation += `\nÁõ∏‰ººÂ∫¶ËåÉÂõ¥: ${info.similarity_range}`;
          }
        }

        setSearchInfo({
          original_query: query,
          ai_explanation: data.service_available 
            ? explanation
            : 'ÂêëÈáèÊêúÁ¥¢ÊúçÂä°‰∏çÂèØÁî®ÔºåÂ∑≤‰∏∫ÊÇ®ÊâßË°åÊôÆÈÄöÊêúÁ¥¢',
          search_intent: data.service_available ? 'ÂêëÈáèÂåπÈÖç' : 'ÊúçÂä°ÈôçÁ∫ß',
          query_time: data.query_time_ms,
          expanded_queries: [],
          suggestions: [],
        });

        if (data.poses && Array.isArray(data.poses) && data.poses.length > 0) {
          if (onAISearchResult) {
            onAISearchResult(data.poses);
          } else {
            onSearch(query);
          }
        } else {
          console.log('ÂêëÈáèÊêúÁ¥¢Êó†ÁªìÊûúÔºåÊâßË°åÊôÆÈÄöÊêúÁ¥¢');
          onSearch(query);
        }
      } else {
        const errorText = await response.text();
        console.error('ÂêëÈáèÊêúÁ¥¢APIÂìçÂ∫îÈîôËØØ:', response.status, errorText);
        onSearch(query);
      }
    } catch (error) {
      console.error('ÂêëÈáèÊêúÁ¥¢Â§±Ë¥•:', error);
      onSearch(query);
    } finally {
      setIsVectorLoading(false);
    }
  };

  const handleKeyDown = (e: React.KeyboardEvent) => {
    if (!showSuggestions || suggestions.length === 0) return;

    switch (e.key) {
      case 'ArrowDown':
        e.preventDefault();
        setSelectedIndex(prev => 
          prev < suggestions.length - 1 ? prev + 1 : prev
        );
        break;
      case 'ArrowUp':
        e.preventDefault();
        setSelectedIndex(prev => prev > 0 ? prev - 1 : -1);
        break;
      case 'Enter':
        e.preventDefault();
        if (selectedIndex >= 0) {
          const selected = suggestions[selectedIndex];
          setQuery(selected.text);
          onSearch(selected.text);
          setShowSuggestions(false);
        } else {
          handleSubmit(e);
        }
        break;
      case 'Escape':
        setShowSuggestions(false);
        setSelectedIndex(-1);
        break;
    }
  };

  const handleSuggestionClick = (suggestion: SearchSuggestion) => {
    setQuery(suggestion.text);
    onSearch(suggestion.text);
    setShowSuggestions(false);
  };

  const getSuggestionIcon = (type: string) => {
    switch (type) {
      case 'history': return 'üïí';
      case 'tag': return 'üè∑Ô∏è';
      case 'synonym': return 'üí°';
      default: return 'üîç';
    }
  };

  const getSuggestionTypeText = (type: string) => {
    switch (type) {
      case 'history': return 'ÂéÜÂè≤ÊêúÁ¥¢';
      case 'tag': return 'Áõ∏ÂÖ≥Ê†áÁ≠æ';
      case 'synonym': return 'Áõ∏ÂÖ≥ËØçÊ±á';
      default: return '';
    }
  };

  // ÁÇπÂáªÂ§ñÈÉ®ÂÖ≥Èó≠Âª∫ËÆÆ
  useEffect(() => {
    function handleClickOutside(event: MouseEvent) {
      if (
        suggestionsRef.current &&
        !suggestionsRef.current.contains(event.target as Node) &&
        inputRef.current &&
        !inputRef.current.contains(event.target as Node)
      ) {
        setShowSuggestions(false);
      }
    }

    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

return (
  <div className="relative w-full max-w-2xl mx-auto">
    <form onSubmit={handleSubmit}>
      <div className="relative">
        <input
          ref={inputRef}
          type="text"
          value={query}
          onChange={(e) => {
            setQuery(e.target.value);
            setShowSuggestions(true);
            setSelectedIndex(-1);
          }}
          onFocus={() => setShowSuggestions(true)}
          onKeyDown={handleKeyDown}
          placeholder="Êô∫ËÉΩÊêúÁ¥¢Ôºö„Äå‰øèÁöÆÂèØÁà±„Äç„ÄåÂíñÂï°ÂéÖÊãçÁÖß„Äç„ÄåÂùêÂßøÂÜôÁúü„Äç..."
          className="w-full px-4 py-3 pr-20 text-gray-900 bg-white border border-gray-300 rounded-lg focus:outline-none focus:border-blue-500 focus:ring-2 focus:ring-blue-200 transition-all"
          disabled={isLoading || isAiDatabaseLoading}
        />
        
        {/* AIÊï∞ÊçÆÂ∫ì/ÂêëÈáè ÊêúÁ¥¢ÊåâÈíÆ */}
        <button
          type="button"
          onClick={() => {
            if (useVectorSearch) {
              handleVectorSearch();
            } else {
              handleAiDatabaseSearch();
            }
          }}
          disabled={isLoading || isAiDatabaseLoading || isVectorLoading}
          className="absolute right-12 top-1/2 -translate-y-1/2 p-2 text-gray-500 hover:text-purple-600 disabled:opacity-50 transition-colors flex items-center justify-center w-8 h-8"
          title="AIÊô∫ËÉΩÊêúÁ¥¢"
        >
          {isAiDatabaseLoading || isVectorLoading ? (
            <div className="w-5 h-5 border-2 border-purple-600 border-t-transparent rounded-full animate-spin"></div>
          ) : (
            <span className="text-lg">‚ú®</span>
          )}
        </button>

        {/* ÊôÆÈÄöÊêúÁ¥¢ÊåâÈíÆ */}
        <button
          type="submit"
          disabled={isLoading || isAiDatabaseLoading || isVectorLoading}
          className="absolute right-2 top-1/2 -translate-y-1/2 p-2 text-gray-500 hover:text-blue-600 disabled:opacity-50 transition-colors flex items-center justify-center w-8 h-8"
          title="ÊôÆÈÄöÊêúÁ¥¢"
        >
          {isLoading ? (
            <div className="w-5 h-5 border-2 border-blue-600 border-t-transparent rounded-full animate-spin"></div>
          ) : (
            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z" />
            </svg>
          )}
        </button>
      </div>
      <div className="flex items-center mt-2 text-sm">
        <input
          id="vector-toggle"
          type="checkbox"
          className="mr-2"
          checked={useVectorSearch}
          onChange={(e) => setUseVectorSearch(e.target.checked)}
        />
        <label htmlFor="vector-toggle">‰ΩøÁî®ÂêëÈáèÊêúÁ¥¢</label>
      </div>
    </form>

      {/* ÊêúÁ¥¢Âª∫ËÆÆ‰∏ãÊãâÊ°Ü */}
      {showSuggestions && suggestions.length > 0 && (
        <div
          ref={suggestionsRef}
          className="absolute top-full left-0 right-0 mt-1 bg-white border border-gray-300 rounded-lg shadow-lg z-50 max-h-80 overflow-y-auto"
        >
          {suggestions.map((suggestion, index) => (
            <div
              key={`${suggestion.type}-${suggestion.text}`}
              className={`flex items-center px-4 py-3 cursor-pointer hover:bg-gray-50 border-b border-gray-100 last:border-b-0 ${
                index === selectedIndex ? 'bg-blue-50' : ''
              }`}
              onClick={() => handleSuggestionClick(suggestion)}
            >
              <span className="text-lg mr-3">{getSuggestionIcon(suggestion.type)}</span>
              <div className="flex-1">
                <div className="text-gray-900">{suggestion.text}</div>
                <div className="text-xs text-gray-500">{getSuggestionTypeText(suggestion.type)}</div>
              </div>
              <div className="text-xs text-gray-400">
                ÊùÉÈáç: {suggestion.weight}
              </div>
            </div>
          ))}
        </div>
      )}

      {/* ÊêúÁ¥¢‰ø°ÊÅØÂ±ïÁ§∫ */}
      {showSearchInfo && searchInfo && (
        <div className="mt-2 p-3 bg-gray-50 rounded-lg text-sm">
          {searchInfo.ai_explanation && (
            <div className="text-green-600 mb-2">
              ü§ñ AIËß£ÈáäÔºö{searchInfo.ai_explanation}
            </div>
          )}
          {searchInfo.search_intent && (
            <div className="text-purple-600 mb-2">
              üéØ ÊêúÁ¥¢ÊÑèÂõæÔºö{searchInfo.search_intent}
            </div>
          )}
          {searchInfo.query_time && (
            <div className="text-gray-500">
              ‚è±Ô∏è Êü•ËØ¢Êó∂Èó¥Ôºö{searchInfo.query_time}ms
            </div>
          )}
        </div>
      )}
    </div>
  );
};

export default EnhancedSearchBar;
